private void cargarProductos() {
DefaultTableModel model = (DefaultTableModel) tabla.getModel();
    model.setRowCount(0); 

    Connection conn = null;
    PreparedStatement ps = null;
    ResultSet rs = null;

    try {
        conn = conexion.connect();
        String sql = "SELECT id_producto, Nombre, Descripci贸n, Precio, stock FROM producto";
        ps = conn.prepareStatement(sql);
        rs = ps.executeQuery();
        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("id_producto"),    
                rs.getString("Nombre"),
                rs.getString("Descripci贸n"),
                rs.getDouble("Precio"),
                rs.getInt("stock")         
            });
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al cargar productos: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    } finally {
        try {
            if (rs != null) rs.close();
            if (ps != null) ps.close();
            if (conn != null) conn.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
}
